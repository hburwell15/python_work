import random

random_integer = random.randint(1,10)
#Will return a random whole number between the spcified numbers in the parenthesis, and will INCLUDE the specified numbers
print(random_integer)

random_float = random.random()
#Will return a floating point number between 0 and 1, but will NOT include 0 and 1. This function also does not take arguments
print(random_float)
